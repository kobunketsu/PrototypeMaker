- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath {
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:@"Cell" forIndexPath:indexPath];
    
    // Configure the cell...
    PMCategory *cat = [PMDataModel current].protoCategories[indexPath.row];
    cell.textLabel.text = cat.title;
    return cell;
}

- (void)tableView:(UITableView *)tableView didSelectRowAtIndexPath:(NSIndexPath *)indexPath{
    PMCategory *category = [PMDataModel current].protoCategories[indexPath.row];
    [PMDataViewController sharedInstance].curCategory = category;
    if (self.delegate) {
        [self.delegate didSelectProtoCategory:category];
    }
}
